package collectionframework.sorting.comparator.lambdaexpression;

public class Employe1 {
	int id;
	String name;
	double sal;
	String jd;
	public Employe1(int id, String name, double sal, String jd) {
		super();
		this.id = id;
		this.name = name;
		this.sal = sal;
		this.jd = jd;
	}
	@Override
	public String toString() {
		return "\nEmploye1 [id=" + id + ", name=" + name + ", sal=" + sal + ", jd=" + jd + "]";
	}
	
}
